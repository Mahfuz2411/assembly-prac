ORG 100H
INCLUDE 'EMU8086.INC'

;REMEMBER SOME NOTES:
;SCAN_NUM USE CX REGISTER TO INPUT
;PRINT_NUM_UNS USE AX REGISTER TO PRINT 

STR1 DB 'ABCDEFGHI','$'    
STR2 DB 'EFGHIJ','$'

PRINT 'STRING ONE: '
LEA DX,STR1  
MOV AH,09H
INT 21H
PRINTN  

PRINT 'STRING TWO: '
LEA DX,STR2  
MOV AH,09H
INT 21H
PRINTN



MOV SI,0
MOV AX,0
MOV BX,0
MOV CX,0  
JMP MAIN_PROCESS
EXTRA_TASK:
    INC AX

MAIN_PROCESS:
    MOV SI,AX
    CMP STR1[SI],'$'
    JE NOT_SUBSTR
    
    MOV BX,AX
    MOV CX,0
    JMP CHECK_SUBSTR
    

CHECK_SUBSTR:     
    MOV SI,CX
    MOV DL,STR2[SI]  
    
    CMP DL,'$'
    JE SUBSTR   
    
    MOV SI,BX   
    
    INC BX
    INC CX
    
    CMP DL,STR1[SI]
    JNE EXTRA_TASK
    JE CHECK_SUBSTR  
    
    
NOT_SUBSTR:
    PRINTN 'DECISION: NOT SUBSTRING'
    JMP DONE

SUBSTR:
    PRINTN 'DECISION: SUBSTRING'

DONE:  
    
    
    
        
    

PRINTN
PRINTN
PRINT 'THANKS FOR RUN THIS CODE...$'
DEFINE_SCAN_NUM      
DEFINE_PRINT_NUM_UNS 
