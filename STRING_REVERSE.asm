    CMP STR[SI],'$'
    JE PRINT_LENGTH  
        
    INC SI    
    JMP FIND_LENGTH
    

PRINT_LENGTH: 
    XOR AX,AX
    PRINT 'YOUR STRING LENGTH IS: $'
    MOV AX,SI
    CALL PRINT_NUM_UNS
    PRINTN 
    

MOV CX,0
