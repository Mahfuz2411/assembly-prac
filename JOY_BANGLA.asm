EXT_PROCESS:
    MOV BX,AX
    CMP AX,1
    JE CHECK_MAX
    
    XOR DX,DX  
    DIV CX
    MUL CX
    
    CMP AX,BX     
    JE EVN
    JNE ODD

;IFF VALUE IS EVEN    
EVN:
    MOV AX,BX
    DIV CX
    INC CNT[0] 
    JMP EXT_PROCESS

;IFF VALUE IS ODD
ODD:
    MOV AX,BX
    MUL CX
    ADD AX,BX
    INC AX 
    INC CNT[0] 
    JMP EXT_PROCESS
      
         
;CHECKING THE MAXIMUM     
CHECK_MAX: 
    MOV AX,CNT[0]
    MOV BX,MAX[0]
    
    CMP AX,BX
    JG STORE_VALUE 
    
    JMP MAIN_PROCESS 
    
;STORING MAXIMUM VALUE
STORE_VALUE:  
    MOV MAX[0],AX 
    
    
;MAIN PROCESS START FROM HERE
MAIN_PROCESS: 
    MOV CNT[0],1     
    MOV AX,LOW[0] 
    
    
    INC LOW[0] 
    
    CMP AX,HGH[0]
    JG DONE
    
    ;CALL PRINT_NUM_UNS
